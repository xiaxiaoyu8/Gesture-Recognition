# 计算两个指尖的距离与基准距离的比值
def getFingerDistance(finger_landmark1, finger_landmark2, landmarks):
    """
    计算两个手指指尖之间的距离与基准距离（手腕到食指根部）的比值。

    参数:
        finger_landmark1: 第一个手指指尖的坐标 (x, y)。
        finger_landmark2: 第二个手指指尖的坐标 (x, y)。
        landmarks: 手部关键点坐标 (21, 2) 的 numpy 数组。

    返回值:
        两个指尖之间的距离与基准距离的比值。如果基准距离为0，则返回0以避免错误。
    """
    # 1. 计算两个指尖之间的绝对距离
    try:
        absolute_distance = math.sqrt(
            (finger_landmark1[0] - finger_landmark2[0]) ** 2 + (finger_landmark1[1] - finger_landmark2[1]) ** 2)
    except IndexError:
        print("Error: Invalid landmark format for finger tips.")
        return 0.0  # Return a default value on error

    # 2. 计算基准距离：手腕 (landmark 0) 到食指根部 (landmark 5)
    try:
        wrist = landmarks[0]
        index_finger_mcp = landmarks[5]  # MCP joint of the index finger
        reference_distance = math.sqrt((wrist[0] - index_finger_mcp[0]) ** 2 + (wrist[1] - index_finger_mcp[1]) ** 2)
    except IndexError:
        print("Error: Landmarks array does not contain required points (0 and 5).")
        return 0.0  # Return a default value on error

    return absolute_distance / reference_distance if reference_distance != 0 else 0.0


